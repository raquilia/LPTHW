# Here's some new strange stuff, remember to type it exactly.

days = "Mon Tue Wed Thu Fri Sat Sun"
# \n produces a new line for each value that succeeds it
# It's officially called an 'escape sequence'
months = "\nJan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

print("Here are the days: ", days)
print("Here are the months: ", months)

# The three """ allow you to continue to print without writing print constantly
print("""
There's something going on here.
With the three double-quotes.
We'll be able to type as much as we like.
Even 4 lines if we want, or 5, or 6.
""")
